class DataAyah:
    def __init__(self, nama, usia, pekerjaan):
        self.nama = nama
        self.usia = usia
        self.pekerjaan = pekerjaan
    def cetakDataAyah(self):
        print("====== Data Ayah ======")
        print("Nama Ayah :",self.nama)
        print("Usia Ayah :",self.usia)
        print("Pekerjaan Ayah :",self.pekerjaan)

